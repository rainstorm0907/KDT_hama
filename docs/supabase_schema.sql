-- Hama Supabase/Postgres schema
-- Supabase Auth(auth.users)를 메인 사용자 원본으로 두고, public.users는 앱 프로필로 사용합니다.
-- 실제 적용 기준 파일은 code/supabase/migrations/*.sql 입니다.

create table if not exists public.users (
  user_id uuid primary key references auth.users(id) on delete cascade,
  login_id text unique,
  email text not null unique,
  name text not null,
  birth_date date,
  phone_number text,
  nickname text not null unique,
  role text not null default 'USER',
  privacy_agreed_at timestamptz not null default now(),
  marketing_agreed_at timestamptz,
  account_status text not null default 'ACTIVE',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.items (
  item_id bigint generated by default as identity primary key,
  platform_name text not null,
  original_id text not null,
  canonical_name text not null,
  title text not null,
  current_price integer not null check (current_price >= 0),
  lowest_price integer check (lowest_price is null or lowest_price >= 0),
  status text not null default '판매중',
  sold_at timestamptz,
  description text,
  category_name text,
  matched_keywords text,
  thumbnail_url text,
  item_url text not null,
  url_checked_at timestamptz,
  url_status text,
  crawled_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  cluster_product_name text,
  cluster_confidence numeric(4, 2),
  cluster_score numeric(5, 2),
  price_score numeric(5, 2),
  view_score numeric(5, 2),
  recency_score numeric(5, 2),
  view_count integer not null default 0,
  rating numeric(6, 2) not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint uq_items_platform_original unique (platform_name, original_id)
);

create index if not exists idx_items_canonical_name on public.items (canonical_name);
create index if not exists idx_items_platform_name on public.items (platform_name);
create index if not exists idx_items_crawled_at on public.items (crawled_at desc);
create index if not exists idx_items_status on public.items (status);
create index if not exists idx_items_rating_desc on public.items (rating desc);
create index if not exists idx_items_cluster_product_name on public.items (cluster_product_name);

create table if not exists public.price_history (
  history_id bigint generated by default as identity primary key,
  item_id bigint not null references public.items(item_id) on delete cascade,
  price integer not null check (price >= 0),
  recorded_at date not null default current_date,
  created_at timestamptz not null default now(),
  constraint uq_price_history_item_date unique (item_id, recorded_at)
);

create index if not exists idx_price_history_item_date
  on public.price_history (item_id, recorded_at);

create table if not exists public.wishlists (
  wish_id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(user_id) on delete cascade,
  item_id bigint not null references public.items(item_id) on delete cascade,
  target_price integer check (target_price is null or target_price >= 0),
  is_lowest_alert boolean not null default true,
  added_at timestamptz not null default now(),
  constraint uq_wishlists_user_item unique (user_id, item_id)
);

create table if not exists public.search_logs (
  log_id bigint generated by default as identity primary key,
  user_id uuid references public.users(user_id) on delete set null,
  keyword text not null,
  clicked_item_id bigint references public.items(item_id) on delete set null,
  created_at timestamptz not null default now()
);

create index if not exists idx_search_logs_user_created
  on public.search_logs (user_id, created_at desc);

create table if not exists public.search_rankings (
  rank_id bigint generated by default as identity primary key,
  keyword text not null unique,
  search_count integer not null default 0,
  trend_status text,
  updated_at timestamptz not null default now()
);

create table if not exists public.user_preferences (
  pref_id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(user_id) on delete cascade,
  preferred_tag text not null,
  constraint uq_user_preferences_user_tag unique (user_id, preferred_tag)
);

create table if not exists public.banners (
  banner_id bigint generated by default as identity primary key,
  image_url text not null,
  link_url text,
  display_order integer not null default 0,
  is_active boolean not null default true
);

create table if not exists public.notifications (
  notification_id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(user_id) on delete cascade,
  item_id bigint references public.items(item_id) on delete set null,
  notification_type text not null,
  title text not null,
  message text,
  payload jsonb,
  is_read boolean not null default false,
  send_status text not null default 'PENDING',
  created_at timestamptz not null default now(),
  sent_at timestamptz,
  read_at timestamptz
);

create index if not exists idx_notifications_user_created
  on public.notifications (user_id, created_at desc);

create table if not exists public.notification_settings (
  setting_id bigint generated by default as identity primary key,
  user_id uuid not null unique references public.users(user_id) on delete cascade,
  lowest_price_enabled boolean not null default true,
  sold_status_enabled boolean not null default true,
  new_item_enabled boolean not null default true,
  updated_at timestamptz not null default now()
);

create table if not exists public.keyword_alerts (
  keyword_alert_id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(user_id) on delete cascade,
