);

create table if not exists public.item_views (
  view_id bigint generated by default as identity primary key,
  user_id uuid not null references public.users(user_id) on delete cascade,
  item_id bigint not null references public.items(item_id) on delete cascade,
  viewed_at timestamptz not null default now(),
  constraint uq_item_views_user_item unique (user_id, item_id)
);

create index if not exists idx_item_views_user_viewed
  on public.item_views (user_id, viewed_at desc);

create table if not exists public.chat_history (
  chat_id bigint generated by default as identity primary key,
  user_id uuid references public.users(user_id) on delete set null,
  user_message text,
